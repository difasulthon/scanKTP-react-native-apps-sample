class Memo {
  constructor(id, name, wilayah, nik, nama, birthdate, kelamin, alamat) {
    this.id = id;
    this.name = name;
    this.wilayah = wilayah;
    this.nik = nik;
    this.nama = nama;
    this.birthdate = birthdate,
    this.kelamin = kelamin,
    this.alamat = alamat
  }
}

export default Memo;